public class Seaching
{
  
  public static void main(String[] args)
  {
    int count = 30;
    int[] nums = new int[count];
    for (int i=0; i < nums.length; i++)
    {
      nums[i] = i*i;
    }
    
    for (int i=0; i < nums.length; i++)
    {
      System.out.println(i + " " + nums[i]);
    }
  }
}